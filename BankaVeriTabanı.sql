-- This query gives the names of the customers who have used loans and the names of the credits they used.
SELECT C.CSTNAMESURNAME AS NAME_SURNAME,L.LOANNAME AS LOAN_NAME
FROM CUS_LOAN AS CL,CUSTOMERS AS C,LOANS AS L
WHERE CL.CUSTOMERID = C.CUSTOMERID
AND CL.LOANID = L.LOANID; 
-- ----------------------------------------------------------------------------------------------
-- This query gives the name, surname, credit card name and limit of the customer with the highest credit card limit.
SELECT C.CSTNAMESURNAME AS NAME_SURNAME,CD.CREDITCARDTYPE AS CREDITCARD_NAME,CD.LIM
FROM CUSTOMERS C,CREDITCARDS CD, CUS_CR CR
WHERE (CR.CUSTOMERID = C.CUSTOMERID AND CD.CREDITCARDID = CR.CREDITCARDID) 
AND LIM = (SELECT MAX(LIM) FROM CREDITCARDS);
-- ----------------------------------------------------------------------------------------------
-- This query gives the initial budget according to the monthly interest account formula
-- and the current budget after the calculation,
-- taking into account the interest rates of the accounts with term accounts.
SELECT ACCOUNTNAME,BUDGET,BUDGET+(BUDGET*ACCINTERESTRATE*ACCMATURITYRATE/(100*12)) "Principal Amount at Maturity"
FROM ACCOUNTS; 
-- Interest calculation formula = (principal amount * interest rate * maturity period (monthly)) / (100*12)
