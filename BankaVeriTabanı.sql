

CREATE SCHEMA Bank;
USE Bank;

-- ----------------------------------------------------------------------------------------------
-- The departments table stores the information of the units in a bank.
CREATE TABLE DEPARTMENTS (DEPARTMENTID INT NOT NULL, DEPARTMENTNAME varchar(30) NOT NULL, BUDGET INT,
NUMOFEMP INT, DEPTMANAGER INT,
 PRIMARY KEY (DEPARTMENTID));

-- 
-- ----------------------------------------------------------------------------------------------
-- The employees table stores information about the people working in the various departments of a bank.
CREATE TABLE EMPLOYEES (EMPLOYEEID INT NOT NULL, NAMESURNAME varchar(20) NOT NULL, TLPHONENO varchar(11),
 EMAIL varchar(50), TITLEID INT,BRANCHID INT NOT NULL, DEPARTMENTID INT NOT NULL,
 PRIMARY KEY (EMPLOYEEID),
 -- FOREIGN KEY (TITLEID) REFERENCES TITLES(TITLEID),
 -- FOREIGN KEY(BRANCHID) REFERENCES BRANCHES(BRANCHID),
 FOREIGN KEY (DEPARTMENTID) REFERENCES DEPARTMENTS(DEPARTMENTID));
 
 -- ----------------------------------------------------------------------------------------------
 -- Branches keep the information of the bank branches served by the banks.
 CREATE TABLE BRANCHES (BRANCHID INT NOT NULL, BRANCHNAME varchar(30),LOCATION varchar(20),NMOFEMP INT,
 PRIMARY KEY (BRANCHID));
 -- ----------------------------------------------------------------------------------------------
 -- Titles are the titles that employees working in the bank take in order to work in a certain hierarchy.
 CREATE TABLE TITLES (TITLEID INT NOT NULL, TITLENAME varchar(20),
 PRIMARY KEY (TITLEID));
 
  -- ----------------------------------------------------------------------------------------------
  -- The loans table keeps the information about the used loans in a bank.
  CREATE TABLE LOANS (LOANID INT NOT NULL,LOANNAME VARCHAR(30),LOANBUDGET INT,INTERESTRATE INT,MTRPERIOD INT,
  PRIMARY KEY (LOANID));
  ALTER TABLE LOANS MODIFY COLUMN INTERESTRATE DOUBLE;
  -- I realized I had to double the interest rates on the loans
  -- ----------------------------------------------------------------------------------------------
  -- The customers table keeps information about the people who use any product of the bank.
  CREATE TABLE CUSTOMERS (CUSTOMERID INT NOT NULL,CSTNAMESURNAME varchar(30),GENDER varchar(15),SSN VARCHAR(11),
  ADDRES VARCHAR(30),PRIMARY KEY (CUSTOMERID));
  ALTER TABLE CUSTOMERS ADD UNIQUE (SSN);
  -- I realized later that the SSN has to be unique.
  -- ----------------------------------------------------------------------------------------------
  -- The credit cards table keeps information about credit cards actively issued to customers in a bank.
  CREATE TABLE CREDITCARDS (CREDITCARDID INT NOT NULL,CREDITCARDTYPE varchar(30),CARTINTERESTRATE INT,
  PRIMARY KEY (CREDITCARDID));
  ALTER TABLE CREDITCARDS ADD LIM INT; -- I decided to set a limit for credit cards.
  ALTER TABLE CREDITCARDS MODIFY COLUMN CARTINTERESTRATE DOUBLE; 
  -- I realized I had to double the interest rates on the cards
   -- ----------------------------------------------------------------------------------------------
   -- The accounts table keeps information about the accounts opened by the customers.
   -- Accounts are divided into two as time and demand accounts.
   CREATE TABLE ACCOUNTS (ACCOUNTID INT NOT NULL,ACCOUNTNAME varchar(30),ACCINTERESTRATE INT,ACCMATURITYRATE INT,
   BRANCHID INT NOT NULL, BUDGET INT,
   PRIMARY KEY (ACCOUNTID),
   FOREIGN KEY (BRANCHID) REFERENCES BRANCHES (BRANCHID));
   ALTER TABLE ACCOUNTS MODIFY COLUMN ACCINTERESTRATE DOUBLE;
   ALTER TABLE ACCOUNTS ALTER ACCINTERESTRATE SET DEFAULT 0;
   ALTER TABLE ACCOUNTS ALTER ACCMATURITYRATE SET DEFAULT 0;
   
    -- ----------------------------------------------------------------------------------------------
    -- A bank has automatic payment instructions created by customers.
    -- Auto Payment Instructions table keeps information about these automatic payment orders.
    CREATE TABLE AUTOPYMNTINSTS (INSTRUCTIONID INT NOT NULL,INSTRUCTIONNAME varchar(30),INSTRUCTIONTYPE varchar(30),
    INVOICEAMOUNT INT,PAYMENTDUEDATE date NOT NULL,PRIMARY KEY (INSTRUCTIONID)); 
    -- ----------------------------------------------------------------------------------------------
    -- The pos machines table keeps the information of the pos devices given to the customers
    -- by the branches of the bank for the customers to use.
    CREATE TABLE POSMACHINES (MACHINEID INT NOT NULL,BRAND varchar(30),COMMISIONRATE INT,BRANCHID INT,
    PRIMARY KEY (MACHINEID),
    FOREIGN KEY (BRANCHID) REFERENCES BRANCHES (BRANCHID));
    ALTER TABLE POSMACHINES MODIFY COLUMN COMMISIONRATE DOUBLE;
    -- ----------------------------------------------------------------------------------------------
    -- This table shows us which Loans the customers have.
    CREATE TABLE CUS_LOAN (CUSTOMERID INT,LOANID INT, 
    FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMERS (CUSTOMERID),
    FOREIGN KEY (LOANID) REFERENCES LOANS (LOANID));
    ALTER TABLE CUS_LOAN ADD UNIQUE (LOANID);
    -- ----------------------------------------------------------------------------------------------
    -- This table shows us which Credit cards the customers have.
    CREATE TABLE CUS_CR (CUSTOMERID INT,CREDITCARDID INT,
    FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMERS (CUSTOMERID),
    FOREIGN KEY (CREDITCARDID) REFERENCES CREDITCARDS (CREDITCARDID),UNIQUE (CREDITCARDID));
    -- ----------------------------------------------------------------------------------------------
    -- This table shows us which Accounts the customers have.
    CREATE TABLE CUS_ACC (CUSTOMERID INT,ACCOUNTID INT,
    FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMERS (CUSTOMERID),
    FOREIGN KEY (ACCOUNTID) REFERENCES ACCOUNTS (ACCOUNTID),UNIQUE (ACCOUNTID));
    -- ----------------------------------------------------------------------------------------------
    -- This table shows us which Pos Machines the customers have.
     CREATE TABLE CUS_POS (CUSTOMERID INT,MACHINEID INT,
     FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMERS (CUSTOMERID),
     FOREIGN KEY (MACHINEID) REFERENCES POSMACHINES (MACHINEID),UNIQUE (MACHINEID));
      -- ----------------------------------------------------------------------------------------------
     -- This table shows us which Auto Payment Instructions the customers have. 
      CREATE TABLE CUS_API (CUSTOMERID INT,INSTRUCTIONID INT,
      FOREIGN KEY (CUSTOMERID) REFERENCES CUSTOMERS (CUSTOMERID),
      FOREIGN KEY (INSTRUCTIONID) REFERENCES AUTOPYMNTINSTS (INSTRUCTIONID),UNIQUE (INSTRUCTIONID));
      -- ----------------------------------------------------------------------------------------------
	  -- This table shows us which automatic payment instruction is used from which account of the customer.
      CREATE TABLE ACC_API (ACCOUNTID INT,INSTRUCTIONID INT,
      FOREIGN KEY (ACCOUNTID) REFERENCES ACCOUNTS (ACCOUNTID),
      FOREIGN KEY (INSTRUCTIONID) REFERENCES  AUTOPYMNTINSTS (INSTRUCTIONID), UNIQUE (INSTRUCTIONID));
      -- ----------------------------------------------------------------------------------------------
